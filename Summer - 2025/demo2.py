#Write a Python program to create, modify, sort, and reverse a list.[7]

#create list
list1 = [1,2,54,7,8,9]
print("Created List:", list1)

#modify List
list1[2] = 100
print("Modified List:", list1)

#sort list
list1.sort()
print("Sorted List:", list1)

#reverse list
list1.reverse()
print("Reversed List:", list1)